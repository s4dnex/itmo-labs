SELECT
    "Н_ОЦЕНКИ"."КОД",
    "Н_ВЕДОМОСТИ"."ЧЛВК_ИД"
FROM "Н_ОЦЕНКИ"
    RIGHT JOIN "Н_ВЕДОМОСТИ" ON ("Н_ОЦЕНКИ"."КОД" = "Н_ВЕДОМОСТИ"."ОЦЕНКА")
WHERE ("Н_ОЦЕНКИ"."КОД" < 'зачет'
    AND "Н_ВЕДОМОСТИ"."ДАТА" < '2022-06-08');

