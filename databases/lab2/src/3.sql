-- 3. Составить запрос, который ответит на вопрос, есть ли среди студентов группы 3102 те, кто старше 25 лет.

SELECT
    CASE
        WHEN COUNT(*) > 0 THEN 'ДА'
        ELSE 'НЕТ'
        END AS "ОТВЕТ",
    COUNT(*) AS "КОЛ-ВО"
FROM "Н_УЧЕНИКИ"
    INNER JOIN "Н_ЛЮДИ" ON ("ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД")
WHERE
    ("ПРИЗНАК" = 'обучен'
    AND "ГРУППА" = '3102'
    AND EXTRACT(YEAR FROM AGE("ДАТА_РОЖДЕНИЯ")) > 25);